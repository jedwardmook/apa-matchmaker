class MatchesController < ApplicationController
  before_action :set_match, only: %i[ show update destroy ]

  # GET /matches
  def index
    @matches = Match.all

    render json: @matches
  end

  # GET /matches/1
  def show
    render json: @match
  end

  # POST /matches
  def create
    @match = Match.new(create_match_params)

    if @match.save
      render json: @match, status: :created, location: @match
    else
      render json: @match.errors, status: :unprocessable_entity
    end
  end

  # PATCH/PUT /matches/1
  def update
    if @match.update(match_params)
      render json: @match
    else
      render json: @match.errors, status: :unprocessable_entity
    end
  end

  # DELETE /matches/1
  def destroy
    @match.destroy
  end

  private
    # Use callbacks to share common setup or constraints between actions.
    def set_match
      @match = Match.find(params[:id])
    end

    # Only allow a list of trusted parameters through.
    def create_params
      params.require(:match).permit(:lag_winner_id, :lag_loser_id, :lag_winner_points_earned, :lag_loser_points_earned, :lag_winner_games_won, :lag_loser_games_won, :session_id, :match_date)
    end
end
